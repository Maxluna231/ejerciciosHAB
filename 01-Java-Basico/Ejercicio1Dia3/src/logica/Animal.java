
package logica;

public class Animal {
    int id_animal;
    String nombre;
    String especie;
    String color;

    public Animal() {
    }

    public Animal(int id_animal,String nombre, String especie, String color) {
        this.id_animal = id_animal;
        this.nombre = nombre;
        this.especie = especie;
        this.color = color;
    }
    
    
    
}
